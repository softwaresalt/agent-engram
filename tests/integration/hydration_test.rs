/// Helper: create an embedded SurrealDB isolated to a temp directory.
async fn test_db(dir: &std::path::Path) -> Queries {
    let db = Surreal::new::<SurrealKv>(dir.to_str().unwrap())
        .await
        .expect("embedded db");
    db.use_ns("test").use_db("hydration").await.expect("ns/db");
    db.query(schema::DEFINE_TASK).await.expect("schema task");
    db.query(schema::DEFINE_CONTEXT)
        .await
        .expect("schema context");
    db.query(schema::DEFINE_RELATIONSHIPS)
        .await
        .expect("schema rels");
    Queries::new(db)
}

fn make_task(id: &str, status: TaskStatus, desc: &str) -> Task {
    let now = Utc::now();
    Task {
        id: id.to_string(),
        title: format!("Task {id}"),
        status,
        work_item_id: None,
        description: desc.to_string(),
        context_summary: None,
        created_at: now,
        updated_at: now,
    }
}

// ─── T058: Integration test for hydration from .tmem/ files ─────────────────

#[test]
async fn hydrate_from_tmem_files_loads_tasks_into_db() {
    let workspace = tempfile::tempdir().expect("workspace tempdir");
    let db_dir = tempfile::tempdir().expect("db tempdir");

    // Create .git/ so the workspace looks valid
    fs::create_dir(workspace.path().join(".git")).expect("create .git");

    // Create .tmem/ with tasks.md
    let tmem_dir = workspace.path().join(".tmem");
    fs::create_dir_all(&tmem_dir).expect("create .tmem");
    fs::write(
        tmem_dir.join("tasks.md"),
        r#"# Tasks

## task:t1

---
id: task:t1
title: First task
status: in_progress
created_at: 2026-02-05T10:00:00+00:00
updated_at: 2026-02-05T14:30:00+00:00
---

First task description.

## task:t2

---
id: task:t2
title: Second task
status: todo
created_at: 2026-02-05T11:00:00+00:00
updated_at: 2026-02-05T11:00:00+00:00
---

Second task description.
"#,
    )
    .expect("write tasks.md");

    // Create graph.surql with a dependency
    fs::write(
        tmem_dir.join("graph.surql"),
        r#"-- Generated by T-Mem
RELATE task:t2->depends_on->task:t1 SET type = 'hard_blocker';
"#,
    )
    .expect("write graph.surql");

    // Write version
    fs::write(tmem_dir.join(".version"), SCHEMA_VERSION).expect("write version");

    // Hydrate summary (lightweight)
    let summary = hydrate_workspace(workspace.path())
        .await
        .expect("hydrate summary");
    assert_eq!(summary.task_count, 2);

    // Hydrate into DB
    let queries = test_db(db_dir.path()).await;
    let result = hydrate_into_db(workspace.path(), &queries)
        .await
        .expect("hydrate into db");
    assert_eq!(result.tasks_loaded, 2);
    assert_eq!(result.edges_loaded, 1);

    // Verify tasks are in DB
    let t1 = queries.get_task("t1").await.expect("get t1");
    assert!(t1.is_some());
    let t1 = t1.unwrap();
    assert_eq!(t1.title, "First task");
    assert_eq!(t1.status, TaskStatus::InProgress);
    assert!(t1.description.contains("First task description"));

    let t2 = queries.get_task("t2").await.expect("get t2");
    assert!(t2.is_some());
    assert_eq!(t2.unwrap().status, TaskStatus::Todo);

    // Verify dependency edge
    let deps = queries.dependencies_of("t2").await.expect("deps of t2");
    assert_eq!(deps.len(), 1);
    assert_eq!(deps[0].to, "t1");
    assert_eq!(deps[0].kind, DependencyType::HardBlocker);
}

